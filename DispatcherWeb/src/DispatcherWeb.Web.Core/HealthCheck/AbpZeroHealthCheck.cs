using DispatcherWeb.HealthChecks;
using Microsoft.Extensions.DependencyInjection;

namespace DispatcherWeb.Web.HealthCheck
{
    public static class AbpZeroHealthCheck
    {
        public static IHealthChecksBuilder AddAbpZeroHealthCheck(this IServiceCollection services)
        {
            var builder = services.AddHealthChecks();
            builder.AddCheck<DispatcherWebDbContextHealthCheck>("Database Connection");
            builder.AddCheck<DispatcherWebDbContextUsersHealthCheck>("Database Connection with user check");
            builder.AddCheck<CacheHealthCheck>("Cache");

            // add your custom health checks here
            // builder.AddCheck<MyCustomHealthCheck>("my health check");

            return builder;
        }
    }
}
